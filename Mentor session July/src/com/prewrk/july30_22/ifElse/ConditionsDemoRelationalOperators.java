package com.prewrk.july30_22.ifElse;

public class ConditionsDemoRelationalOperators {

	public static void main(String[] args) {

		boolean condition = (30 < 80);

		if (condition) {
			System.out.println("Condition is true");
		}

		condition = (10 == 10);
		if (condition) {
			System.out.println("Condition is true");
		}

		condition = (30 > 100);
		if (condition) {
			System.out.println("Condition is true");
		}
	}
}
