package com.prewrk.july23_22.datatypes;

public class CharacterDemo1 {

	public static void main(String[] args) {

		char capitalA = 'A';

		System.out.println("Value is ->" + capitalA);

		int intEquivalent = (int) capitalA;

		System.out.println("Integer equivalent ->" + intEquivalent);

		capitalA++;
		System.out.println("Value is ->" + capitalA);
		intEquivalent++;
		System.out.println("Integer equivalent ->" + intEquivalent);

	}
}
