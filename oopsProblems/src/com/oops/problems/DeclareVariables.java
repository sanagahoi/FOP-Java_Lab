package com.gl.javaPrework.week_1;

public class DeclareVariables {
	public static void main(String[] args) {

		int num1 = 1;
		float num2 = 2;
		char Character = 's';
		boolean bool = false;
		String str = "Hello";
		double num3 = 14.57;

		System.out.println("int value is : " + num1);
		System.out.println("float value is : " + num2);
		System.out.println("char value is : " + Character);
		System.out.println("boolean value is : " + bool);
		System.out.println("String value is : " + str);
		System.out.println("double value is : " + num3);
	}

}
